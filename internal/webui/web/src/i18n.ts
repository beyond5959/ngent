let currentLanguage: Language = 'en'

function normalizeLanguageTag(value: string | null | undefined): string {
  return (value ?? '').trim().replace(/_/g, '-').toLowerCase()
}

function matchSupportedLanguage(value: string): Language | null {
  if (!value) return null
  if (value === 'en' || value.startsWith('en-')) return 'en'
  if (value === 'es' || value.startsWith('es-')) return 'es'
  if (value === 'fr' || value.startsWith('fr-')) return 'fr'
  if (value === 'zh-cn' || value === 'zh-hans' || value.startsWith('zh-') || value === 'zh') return 'zh-CN'
  return null
}

export function resolveLanguage(value: string | null | undefined): Language {
  return matchSupportedLanguage(normalizeLanguageTag(value)) ?? 'en'
}

export function detectBrowserLanguage(): Language {
  const candidates = typeof navigator !== 'undefined'
    ? [...(navigator.languages ?? []), navigator.language]
    : []
  for (const candidate of candidates) {
    const match = matchSupportedLanguage(normalizeLanguageTag(candidate))
    if (match) return match
  }
  return 'en'
}

export function setLanguage(language: Language): void {
  currentLanguage = resolveLanguage(language)
  if (typeof document !== 'undefined') {
    document.documentElement.lang = currentLanguage
  }
}

export function getLanguage(): Language {
  return currentLanguage
}

export function getLanguageLabel(language: Language): string {
  return languageLabels[language]
}

export function t(key: string, params: TranslationParams = {}): string {
  const value = translations[currentLanguage][key] ?? translations.en[key] ?? key
  return typeof value === 'function' ? value(params) : value
}
